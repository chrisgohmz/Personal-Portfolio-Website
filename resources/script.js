let lightMode = () => {
    document.querySelector("main").style.backgroundImage = "url('resources/images/background-img-light.jpg')";
    document.querySelector("main").style.color = "black";
}

let darkMode = () => {
    document.querySelector("main").style.backgroundImage = "url('resources/images/background-img-dark.jpg')";
    document.querySelector("main").style.color = "white";
}